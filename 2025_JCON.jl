# ╔═╡ 39d51165-bb39-44c0-824b-3785ef7bbf7d
function matvec(B, z, V)
	ΔV = B' * V
	I_branches = ΔV ./ z
	return B * I_branches
end

# ╔═╡ a3114227-0bee-43fb-a1c3-ae8f197daf38
function matmat(B, z, V)
	Y = B * LinearAlgebra.Diagonal(inv.(z)) * B'
	Y * V
end

